package com.sctrcd.qzr.facts;

public class Option {

    private String key;
    
    private String text;

    public Option() {
    }

    public Option(String key, String text) {
    }

    public String getKey() {
        return key;
    }

    public void setKey(String key) {
        this.key = key;
    }

    public String getText() {
        return text;
    }

    public void setText(String text) {
        this.text = text;
    }

}
